public class Colegio {
    private String col_id;
    private String col_nombre;
    private String col_licencia;

    public String getCol_id() {
        return col_id;
    }

    public void setCol_id(String col_id) {
        this.col_id = col_id;
    }

    public String getCol_nombre() {
        return col_nombre;
    }

    public void setCol_nombre(String col_nombre) {
        this.col_nombre = col_nombre;
    }

    public String getCol_licencia() {
        return col_licencia;
    }

    public void setCol_licencia(String col_licencia) {
        this.col_licencia = col_licencia;
    }
    
    
    
}
